import streamlit as st

st.set_page_config(layout="wide")

def main():
    st.title("여행 가이드 챗봇")
    st.write("### 여행의 동반자")
    st.write(
        """
        안녕하세요! 여행 가이드 챗봇 페이지에 오신 것을 환영합니다. 😊  
        저희 챗봇은 춘천에서의 여행을 더 쉽고 즐겁게 만들어드리기 위해 설계되었습니다.
        """
    )
    
    st.image(
        "20241203.png"
    )
    
    st.write("#### 주요 기능")
    st.markdown(
        """
        - **춘천의 숙소 추천**: 편안한 숙박을 위한 최적의 숙소를 추천합니다.
        - **춘천의 맛집 탐방**: 닭갈비 등 춘천에서 꼭 먹어야 할 맛집을 소개합니다.
        - **춘천의 관광지 정보**: 소양강 스카이워크 등 인기 명소를 알려드립니다.
        """
    )
    st.write("#### 활용 방법")
    st.markdown(
        """
        1. **춘천의 숙소 찾기**: 원하는 조건에 맞는 숙소를 검색해보세요.
        2. **춘천의 맛집 탐색**: 닭갈비 맛집, 막국수 전문점 등 추천을 받아보세요.
        3. **춘천의 관광지 알아보기**: 방문할 만한 명소의 정보를 확인하고 일정에 추가하세요.
        """
    )
    
    st.info("💡 춘천의 숙소, 맛집, 관광지를 한곳에서 간편하게 검색하세요!")
    
    
    # Tabs for 숙소, 맛집, 관광지
    tab1, tab2, tab3 = st.tabs(["숙소 추천", "맛집 탐방", "관광지 정보"])

    with tab1:
        st.write("### 춘천의 숙소 추천")
        st.markdown(
            """
            - **가족 여행 추천 숙소**: 강변 근처 펜션, 편안한 객실과 훌륭한 서비스 제공.
            - **커플을 위한 호텔**: 강변 뷰 호텔, 로맨틱한 분위기.
            - **저렴한 숙소**: 깔끔하고 경제적인 게스트하우스 추천.
            """
        )

    with tab2:
        st.write("### 춘천의 맛집 탐방")
        st.markdown(
            """
            - **춘천 닭갈비 골목**: 춘천 여행에서 빠질 수 없는 닭갈비 전문점.
            - **막국수 맛집**: 춘천의 전통 막국수로 여름 더위를 이기세요.
            - **카페 투어**: 한옥 스타일 카페와 강변 뷰를 즐길 수 있는 카페.
            """
        )

    with tab3:
        st.write("### 춘천의 관광지 정보")
        st.markdown(
            """
            - **백양리**: 북한강으로 이어지는 자연 명소.
            - **소양강 스카이워크**: 소양강을 조망하며 즐기는 스릴 넘치는 경험.
            - **애니메이션 박물관**: 아이들과 함께 가면 좋은 테마 관광지.
            """
        )
        
    st.write("")

    if st.button("메인 페이지 시작하기"):
        st.session_state.page = "chatBotApp"

if __name__ == "__main__":
    main()
